public class MathOperations
{
    public int Add(int a, int b)
    {
        int sum = a + b;

        return sum;
    }

    public double Add(double a, double b, double c)
    {
        double sum = a + b + c;

        return sum;
    }

    public decimal Add(decimal a, decimal b, decimal c)
    {
        decimal sum = a + b + c;

        return sum;
    }
}